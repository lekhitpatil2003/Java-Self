public class BitwiseOperators {
    public static void main(String[] args) {
        
        int a = 2; // 10 --> 00000000 00000000 00000000 00000010
        int b = 3; // 11 --> 00000000 00000000 00000000 00000011

        // Bitwise Operators
        int c = a & b; // 2 --> 10 --> 00000000 00000000 00000000 00000010
        int d = a | b; // 3 --> 11 --> 00000000 00000000 00000000 00000011
        int e = a ^ b; // 1 --> 01 --> 00000000 00000000 00000000 00000001
        int f = ~a; // -3 --> 11111111 11111111 11111111 11111101
        int g = ~b; // -4 --> 11111111 11111111 11111111 11111100

        // Print Output
        System.out.println(c + " , " + d + " , " + e + " , " + f + " , " + g); 

        // Shift Operations
        int h = 1; // 00000000 00000000 00000000 00000001 --> 1
        // Left Shift (<<)
        //h = h << 30; // 01000000 00000000 00000000 00000000 --> 1073741824
        //h = h << 31; // 10000000 00000000 00000000 00000000 --> -2147483648 --> gives Smallest Number
        //h = h << 32; // 00000000 00000000 00000000 00000001 --> 1 --> 32 % 32 = 0; h = h << 0
        //h = h << 33; // 00000000 00000000 00000000 00000001 --> 2 --> 33 % 33 = 1; h = h << 1
        
        System.out.println(h);

        // Right Shift (>>) (>>>)
        byte i = 1; // 00000001
        i = (byte)(i << 1); 
        // 00000000 00000000 00000000 00000010 --> type cast int to byte (truncate [] this part)
        // [00000000 00000000 00000000] 00000010 --> 2

        System.out.println(i);
    }
}